package polimorfismStatic;

public class Gaina {

	public static void haveSomething(int var) {
		System.out.println("Hen has: " + var + " legs");
	}

	public static void haveSomething(String var) {
		System.out.println("Hen say: " + var);
	}

	public static void haveSomething(double var) {
		System.out.println("Hen eat " + var + " kilos");
	}
}
